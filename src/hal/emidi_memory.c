static const int _fileSize = sizeof(pMidi);
static int _filePos;


int eMidi_timeUs() {
  struct timespec t;
  clock_gettime(CLOCK_MONOTONIC, &t);

  return t.tv_sec * 1000000 + t.tv_nsec / 1000;
}

FILE* eMidi_fopen(const char* pPathName, const char* pMode) {
  _filePos = 0;

  return (FILE*)pMidi;
}

int eMidi_fclose(FILE* pStream) {
  _filePos = -1;

  return 0;
}

long eMidi_ftell(FILE* pStream) {
  return _filePos;
}

int eMidi_fseek(FILE* pStream, long offset, int whence) {
  switch(whence) {
    case SEEK_CUR: _filePos += offset;       break;
    case SEEK_SET: _filePos = 0;             break;
    case SEEK_END: _filePos = sizeof(pMidi); break;
      break;

    default:
      return -1;
  }

  return 0;
}

size_t eMidi_fread(void* p, size_t size, size_t nmemb, FILE* pStream) {
  int toRead = nmemb;
  const char* pFile = (const char*)pStream;

  memcpy(p, &pFile[_filePos], toRead);
  _filePos += toRead;

  return toRead;
}

